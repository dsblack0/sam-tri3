package frq20;

public class Q3_ProductReview {
  private String name;
  private String review;

  public Q3_ProductReview(String pName, String pReview) {
    name = pName;
    review = pReview;
  }

  public String getName() {
    return name;
  }

  public String getReview() {
    return review;
  }  
}